		function insertFuliTxt(btnId){
			var conTxt = $('#txtContent').val();
			var insertTxt = '';
			var result = '';
			var reg;
			if(btnId == 'addFuliSave'){
				insertTxt  = '[公司福利：';
				reg =/(\[公司福利：.*\])/g;
				$('.addBox').eq(0).find('.fuliSelList').html($('._dialog .fuliSelList').html());
			} else if(btnId == 'addGwSave'){
				insertTxt = '[岗位职责：';
				reg =/(\[岗位职责：.*\])/g;
				$('.addBox').eq(1).find('.fuliSelList').html($('._dialog .fuliSelList').html());
			}
			$('._dialog .addBox .cu').each(function(){
				if($(this).index() == 0){
					insertTxt += $(this).text();
				} else {
					insertTxt += '，' +  $(this).text() ;
				}
			});
			insertTxt += ']';

			if($.trim(conTxt).length == 0){
				result = insertTxt;
			} else {
				if(reg.test(conTxt)){
					result = conTxt.replace(RegExp.$1,insertTxt);
				} else {
					result = conTxt + '\n\n' + insertTxt;
				}
			}
			$('#txtContent').val(result);
		}



